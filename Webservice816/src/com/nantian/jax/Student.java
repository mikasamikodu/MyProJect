package com.nantian.jax;

import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement
public class Student {

	private String id;
	private String name;
	private Classroom room;
	public Student() {
		super();
	}
	public Student(String id, String name, Classroom room) {
		super();
		this.id = id;
		this.name = name;
		this.room = room;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Classroom getRoom() {
		return room;
	}
	public void setRoom(Classroom room) {
		this.room = room;
	}
	
	
}
